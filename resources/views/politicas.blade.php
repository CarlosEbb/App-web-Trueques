el Tratamiento de Datos Personales por parte de </font></font><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font><font color="#000000"><font size="2" style="font-size: 11pt">
se garantiza el derecho de los Titulares a obtener del Responsable
del Tratamiento o del Encargado del Tratamiento, en cualquier momento
y sin restricciones, información acerca de la existencia de Datos
Personales que le conciernan; </font></font></font></font>
</p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font color="#000000"><font size="2" style="font-size: 11pt">f)</font></font><font color="#000000"><font size="2" style="font-size: 11pt">&nbsp;</font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>Principio
de acceso y circulación restringida:</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>&nbsp;</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt">El
Tratamiento</font></font><font size="2" style="font-size: 11pt"><b> </b></font><font size="2" style="font-size: 11pt">que
desarrolla </font><font size="2" style="font-size: 11pt"><b>Cambiémoslo
</b></font><font color="#000000"><font size="2" style="font-size: 11pt">se
sujeta a los límites que se derivan de la naturaleza de los Datos
Personales, de las disposiciones de la ley y la Constitución.  </font></font></font></font>
</p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font color="#000000"><font size="2" style="font-size: 11pt">Los
Datos Personales que serán objeto de tratamiento por parte de
</font></font><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font><font color="#000000"><font size="2" style="font-size: 11pt">
salvo la información pública, no estarán disponibles en Internet u
otros medios de divulgación o comunicación masiva, salvo que el
acceso sea técnicamente controlable para brindar un conocimiento
restringido sólo a los Titulares o terceros autorizados conforme con
la ley; </font></font></font></font>
</p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font color="#000000"><font size="2" style="font-size: 11pt">g)</font></font><font color="#000000"><font size="2" style="font-size: 11pt">&nbsp;</font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>Principio
de seguridad:</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>&nbsp;</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt">La
información sujeta a Tratamiento parte de </font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt">,
se manejará con las medidas técnicas, humanas y administrativas que
sean necesarias para su seguridad, evitando su adulteración,
pérdida, consulta, uso o acceso no autorizado o fraudulento; </font></font></font></font>
</p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font color="#000000"><font size="2" style="font-size: 11pt">h)</font></font><font color="#000000"><font size="2" style="font-size: 11pt">&nbsp;</font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>Principio
de confidencialidad:</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>&nbsp;</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt">Todas
las personas que intervengan en el Tratamiento de Datos Personales
por parte de </font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>
</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt">han
sido obligadas a garantizar la reserva de la información, inclusive
después de finalizada su relación con alguna de las labores que
comprende el Tratamiento, pudiendo sólo realizar suministro o
comunicación de Datos Personales cuando ello corresponda al
desarrollo de las actividades autorizadas en la ley y en los términos
de la misma. </font></font></font></font>
</p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font color="#000000"><font size="2" style="font-size: 11pt"><b>V.
Tratamiento al Cual Serán Sometidos los Datos. </b></font></font></font></font>
</p>
<p align="JUSTIFY" style="margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font color="#000000"><font size="2" style="font-size: 11pt">1.
</font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt">
solo usará, procesará y circulará los datos personales y otra
información de sus miembros para las finalidades descritas y para
los tratamientos autorizados en esta Política de Privacidad o en las
leyes vigentes. En adición a lo mencionado en otras cláusulas, cada
Titular de la información expresamente autoriza a </font></font><font color="#000000"><font size="2" style="font-size: 11pt"><b>Cambiémoslo
</b></font></font><font color="#000000"><font size="2" style="font-size: 11pt">para
la recolección, uso y circulación de sus datos personales.</font></font></font></font></p>
<p align="JUSTIFY" style="margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font size="2" style="font-size: 11pt"><span style="background: #ffffff">2.
</span></font><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font><font size="2" style="font-size: 11pt"><span style="background: #ffffff">
informa de manera expresa a los Titulares de Datos Sensibles que
proporcionar la información sensible es meramente facultativa y no
están obligados a entrega información relacionada con sus Datos
Sensibles. Sin embargo, para la efectiva selección de personal,
</span></font><font size="2" style="font-size: 11pt"><b><span style="background: #ffffff">Cambiémoslo
</span></b></font><font size="2" style="font-size: 11pt"><span style="background: #ffffff">recomienda
a los Titulares proporcionar toda la información sensible o no que
sea necesaria para una efectiva selección y; </span></font></font></font>
</p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font size="2" style="font-size: 11pt"><span style="background: #ffffff">3.
Los Datos Personales y Datos Sensibles recolectados, dentro de los
cuales podrán además encontrarse información médica y relativa a
la salud, tienen como finalidad (a) la prestación de servicios de
salud; (b) el cumplimiento de protocolos médicos y </span></font><font size="2" style="font-size: 11pt">(c)
el desarrollo de su plataforma para facilitar el intercambio de
productos y servicios entre usuarios; (d) el cumplimiento de
protocolos técnicos para el desarollo de su plataforma para
facilitar el intercambio de productos y servicios entre usuarios; (e)
el desarrollo de su objeto social; (f) el cumplimiento de la
normatividad vigente; (g) propósitos educativos y académicos; (h)
establecer comunicación con los Titulares para cualquier propósito
relacionado con las finalidades que se establecen en la presente
Política de Privacidad, ya sea mediante llamadas, mensajes de texto,
correos electrónicos y/o físicos; (i) enviar información sobre los
productos o servicios comercializados por </font><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font><font size="2" style="font-size: 11pt">,
así como, novedades, noticias, publicaciones y promociones propias o
de terceros; (j) enviar información sobre programas propios y de
terceros; (k) realizar estudios o investigaciones de mercado para uso
académico o comercial; (l) ya sea directamente o a través de
terceros desarrolle las actividades propias de su objeto social en
cuanto a los procesos de selección de personal, nómina y seguridad
social; (m) gestión comercial, suscripción de contratos, registro
de clientes en software de facturación y contabilidad para fines
comerciales y tributarios; (n) gestión de proveedores; y (o)
directamente entre o a través de terceros: consultar, llevar a cabo
trámites administrativos, enviar información comercial, solicitar
diligenciamiento de encuestas, recolectar, almacenar, usar, circular,
suprimir, procesar, contactar, encuestar, compilar, intercambiar,
publicar, auditar, actualizar y/o disponer de los Datos Personales
que los Titulares suministren para incorporarlos en sus distintas
Bases de Datos. </font></font></font>
</p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font size="2" style="font-size: 11pt"><span style="background: #ffffff">4.
</span></font><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font><font size="2" style="font-size: 11pt"><span style="background: #ffffff">
no verifica, ni asume la obligación de verificar la identidad de
quien suministra sus Datos Personales o Datos Sensibles, como tampoco
la vigencia, suficiencia y autenticidad de los Datos Personales o
Datos Sensibles que éste proporcione sobre sí mismo, por tanto,
</span></font><font size="2" style="font-size: 11pt"><b>Cambiémoslo</b></font><font size="2" style="font-size: 11pt"><span style="background: #ffffff">
se excluye de cualquier responsabilidad por los daños y perjuicios
de cualquier naturaleza que pudieran generarse por la falta de
veracidad, vigencia, suficiencia o autenticidad de la información.
Esto incluye los daños y perjuicios que puedan generarse por
suplantación de la identidad de un tercero.</span></font></font></font></p>
<p align="JUSTIFY" style="margin-left: -0.1in; margin-right: -0.06in; margin-top: 0.19in; margin-bottom: 0.19in; background: #ffffff; line-height: 100%">
<font face="Times New Roman, serif"><font size="3"><font size="2" style="font-size: 11pt"><span style="background: #ffffff">5.
